const month = "Mar"

switch (month) {
    case "Jan":
        console.log("January");        
        break;
    case "Feb":
        console.log("Feb");        
        break;
    case "Mar":
        console.log("Mar");        
        break;
    case "Apr":
        console.log("Apr");        
        break;
    case "May":
        console.log("May");        
        break;
    case "Jun":
        console.log("Jun");        
        break;

    default:
        console.log("Default case matched"); 
        break;
}